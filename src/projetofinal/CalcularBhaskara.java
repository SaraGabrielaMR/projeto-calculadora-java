    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        String numero1 = txtTextoA.getText();
        String numero2 = txtTextoB.getText();
        String numero3 = txtTextoC.getText();
        
        int textoA = Integer.parseInt(numero1);
        int textoB = Integer.parseInt(numero2);
        int textoC = Integer.parseInt(numero3);
        int numero = 4;
        
        int calcular = textoB * textoB - numero * textoA * textoC;
        
        String resultado = (String.valueOf(calcular));
        txtResultado.setText(resultado);
    }//GEN-LAST:event_btnCalcularActionPerformed

